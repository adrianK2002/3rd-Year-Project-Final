					<h1 style="color:black;">Common types of exercises</h1>
					
					<p>Common types of exercise
						There are various types of exercise, including:
						<ul style="color:black">
							<li>Aerobic - The core of any fitness program should include some form of continuous movement. Examples include swimming, running, and dancing.
							<li>Strength - These exercises help increase muscle power and strength. Examples include resistance training, plyometrics, weightlifting, and sprinting.
							<li>Calisthenics - These moves are usually performed without gym equipment using large muscle groups. They’re done at a medium aerobic pace. Examples include lunges, situps, pushups, and pullups.
							<li>High-intensity interval training (HIIT) - This type of exercise includes repetitions of short bursts of high-intensity exercise followed by low-intensity exercises or rest periods.
							<li>Boot camps - These are timed-based, high-intensity circuits that combine aerobic and resistance exercises.
							<li>Balance or stability - These exercises are designed to strengthen muscles and improves body coordination. Examples include Pilates, tai chi poses, and core-strengthening exercises.
							<li>Flexibility -These types of exercises help muscle recovery, maintain range of motion, and prevent injuries. Examples include yoga or individual muscle-stretch movements.
						
						<br>
					<br>
						The activities above can be done individually or combined. The important thing is to do what works best for you and to have fun with it.</p>
